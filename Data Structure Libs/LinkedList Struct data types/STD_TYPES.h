#ifndef STD_TYPES_H
#define STD_TYPES_H

/* Signed data types */
typedef signed char 							s8;
typedef signed short int 						s16;
typedef signed int		 						s32;
typedef signed long 							s64;
typedef long double 							s96;
/* Unsigned data types */
typedef unsigned char 							u8;
typedef unsigned short int						u16;
typedef unsigned int							u32;
typedef unsigned long 							u64;
/* Float data types */
typedef float 									f32;
typedef double 									f64;


typedef signed char								ERROR_STATUS;

#define E_NOK									0
#define E_OK									1							

#endif 